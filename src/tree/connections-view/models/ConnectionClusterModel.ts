import { type EmulatorConfiguration } from '../../../utils/emulatorConfiguration';
import { type BaseClusterModel } from '../../models/BaseClusterModel';

/**
 * Cluster model for the Connections View.
 * Simple, clean, no Azure dependencies.
 *
 * This represents user-added connections that are stored in ConnectionStorageService.
 * These connections have a stable storageId that serves as their clusterId for caching.
 */
export interface ConnectionClusterModel extends BaseClusterModel {
    /**
     * Stable identifier in storage - THIS is what becomes clusterId.
     *
     * This is a UUID generated by ConnectionStorageService when the connection
     * is first saved. It remains stable even when the item moves between folders.
     */
    storageId: string;

    /**
     * Emulator configuration (optional).
     * Present when this connection represents a local emulator instance.
     */
    emulatorConfiguration?: EmulatorConfiguration;
}
